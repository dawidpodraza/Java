public class Calculator {

    void add(int x,int y){
        System.out.println(x+y);
    }
    void add(int x,int y,int z){
        System.out.println(x+y+z);
    }

    void subtrack(int x,int y){
        System.out.println(x-y);
    }
    void subtrack(int x,int y,int z){
        System.out.println(x-y-z);
    }
}
