public class CalculateMeasures {
    double metersToCentimeters(double x){
        return x*100;
    }

    double metersToMilimeters(double x){
        return x*1000;
    }

    double centimetersToMeters(double x){
        return x*0.01;
    }
    double milimetersToMeters(double x){
        return x*0.001;
    }
}
